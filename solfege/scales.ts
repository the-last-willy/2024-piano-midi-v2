/**
 * Return the number of semitones between each interval in major scales.
 */
export function majorScale(): number[] {
    return [2, 2, 1, 2, 2, 2, 1];
}

/**
 * Return the number of semitones between each interval in minor scales.
 */
export function minorScale(): number[] {
    return [2, 1, 2, 2, 1, 3, 1];
}
